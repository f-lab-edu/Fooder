DROP TABLE IF EXISTS STORE;
CREATE TABLE `STORE`
(
    `STORE_ID`         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    `NAME`             VARCHAR(100)                                        NOT NULL,
    `OPEN`             BOOLEAN                                             NOT NULL,
    `FOOD_CATEGORY`    VARCHAR(50)                                         NOT NULL,
    `TELEPHONE_NUMBER` VARCHAR(20) NULL,
    `MIN_ORDER_PRICE`  SMALLINT                                            NOT NULL,
    `DELIVERY_PRICE`   SMALLINT                                            NOT NULL
);

DROP TABLE IF EXISTS STORE_ADDRESS;
CREATE TABLE `STORE_ADDRESS`
(
    `STORE_ADDRESS_ID` BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    `STORE_ID`         BIGINT                                              NOT NULL,
    `ZIP_CODE`         VARCHAR(5)                                          NOT NULL,
    `ADDRESS`          VARCHAR(255)                                        NOT NULL,
    `DETAIL_ADDRESS`   VARCHAR(255)                                        NOT NULL
);

DROP TABLE IF EXISTS STORE_TIME;
CREATE TABLE `STORE_TIME`
(
    `STORE_ID`    BIGINT NOT NULL,
    `START_TIME`  TIME   NOT NULL,
    `FINISH_TIME` TIME   NOT NULL,
    FOREIGN KEY (STORE_ID) REFERENCES STORE (STORE_ID)
);


